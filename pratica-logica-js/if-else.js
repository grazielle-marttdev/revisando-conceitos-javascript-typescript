// if.. else
// Estrutura básica de controle de fluxo, onde uma condição é avaliada e, dependendo do resultado, um bloco de código é executado.

let nota = 85;
if (nota >= 70) {
    console.log("Excelente! Você tirou uma nota alta e está aprovada(o).");
} else if (nota >= 50) {
    console.log("Você passou, mas precisa melhorar.");
} else {
    console.log("Infelizmente, você não atingiu a nota mínima para aprovação.");
}

